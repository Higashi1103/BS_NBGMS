package com.example.demo.entity;

public class Yuza {

	private String yuzaId;

	private String yuzaPwd;

	private String yuzaNamae;

	private String sakuseijikan;

	private String ronrisakujo;

	public String getYuzaId() {
		return yuzaId;
	}


	public void setYuzaId(String yuzaId) {
		this.yuzaId = yuzaId;
	}


	public String getYuzaPwd() {
		return yuzaPwd;
	}


	public void setYuzaPwd(String yuzaPwd) {
		this.yuzaPwd = yuzaPwd;
	}


	public String getYuzaNamae() {
		return yuzaNamae;
	}


	public void setYuzaNamae(String yuzaNamae) {
		this.yuzaNamae = yuzaNamae;
	}


	public String getSakuseijikan() {
		return sakuseijikan;
	}


	public void setSakuseijikan(String sakuseijikan) {
		this.sakuseijikan = sakuseijikan;
	}


	public String getRonrisakujo() {
		return ronrisakujo;
	}


	public void setRonrisakujo(String ronrisakujo) {
		this.ronrisakujo = ronrisakujo;
	}
}